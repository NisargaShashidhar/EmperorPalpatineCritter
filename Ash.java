import java.awt.Color;


public class Ash extends Rock
{

    /**
     * Constructs an ash that is the color of the image.
     */
    public Ash()
    {
        setColor(null);
    }

    /**
     * Constructs a rock of a given color.
     * @param rockColor the color of this rock
     */
    public Ash(Color ashColor)
    {
        setColor(ashColor);
    }

}
